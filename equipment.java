
public class equipment {
    String name;
    int health;
    int strength;
    int defence;
    int initiative;
    int type;
    
    public equipment(String name, int health, int strength, int defence, int initiative, int type) {
        this.name = name;
        this.health= health;
        this.strength = strength;
        this.defence = defence;
        this.initiative = initiative;
        this.type = type;
    }

}
